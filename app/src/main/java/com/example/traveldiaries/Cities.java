package com.example.traveldiaries;

public class Cities {
    int id;
    String name;
    String desc;
    byte[] image;

    public Cities(int id, String name, String desc, byte[] image ){
        this.name = name;
        this.desc = desc;
        this.image = image;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public byte[] getImage() {
        return image;
    }

    public void setImage(byte[] image) {
        this.image = image;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
